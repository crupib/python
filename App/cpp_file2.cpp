#include <iostream>
int main() {
    int a = 7;                     // Declare variable a with type int and value 7.
    float b;                       // Declare variable a with type float.
    float result;                  // Declare variable result with type float.
    b = 7.14;                      // Variable b is equal to 7.14.
    result = a + b;                // Addition.
    std::cout << result;           // Result to the Terminal/cmd.
    std::cout << "\nCompleted\n";  // String to the Terminal/cmd.
}
